package com.xuexiang.xuidemo.fragment.expands.materialdesign.itemtouch;

import android.graphics.Rect;
import android.view.View;

import androidx.annotation.NonNull;
import androidx.recyclerview.widget.GridLayoutManager;
import androidx.recyclerview.widget.LinearLayoutManager;
import androidx.recyclerview.widget.RecyclerView;

/**
 * @author xuexiang
 * @since 5/23/23 1:39 AM
 */
public class GridSpaceItemDecoration extends RecyclerView.ItemDecoration {

    private int spanCount;
    private int spacing = 20;
    private boolean includeEdge = false;

    public GridSpaceItemDecoration(int spanCount) {
        this.spanCount = spanCount;
    }

    public GridSpaceItemDecoration(int spanCount, int spacing, boolean includeEdge) {
        this.spanCount = spanCount;
        this.spacing = spacing;
        this.includeEdge = includeEdge;
    }

    @Override
    public void getItemOffsets(@NonNull Rect outRect, @NonNull View view, @NonNull RecyclerView parent, @NonNull RecyclerView.State state) {
        RecyclerView.LayoutManager layoutManager = parent.getLayoutManager();
        if (layoutManager == null) {
            return;
        }

        if (layoutManager instanceof GridLayoutManager) {
            // 获取item在adapter中的位置
            int position = parent.getChildAdapterPosition(view);
            // item所在的列
            int column = position % spanCount;
            if (includeEdge) {
                outRect.left = spacing - column * spacing / spanCount;
                outRect.right = (column + 1) * spacing / spanCount;
                if (position < spanCount) {
                    outRect.top = spacing;
                }
                outRect.bottom = spacing;
            } else {
                outRect.left = column * spacing / spanCount;
                outRect.right = spacing - (column + 1) * spacing / spanCount;
                if (position >= spanCount) {
                    outRect.top = spanCount;
                }
                outRect.bottom = spacing;
            }
        } else if (layoutManager instanceof LinearLayoutManager) {
            outRect.top = spanCount;
            outRect.bottom = spacing;
        }
    }
}
